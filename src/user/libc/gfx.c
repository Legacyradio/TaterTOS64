void gfx_init(gfx_ctx_t *ctx, uint32_t *buffer, uint32_t w, uint32_t h, uint32_t stride) {
    ctx->buffer = buffer;
    ctx->width = w;
    ctx->height = h;
    ctx->stride = stride;
}

void gfx_putpixel(gfx_ctx_t *ctx, uint32_t x, uint32_t y, uint32_t color) {
    if (x >= ctx->width || y >= ctx->height) return;
    ctx->buffer[y * ctx->stride + x] = color;
}

void gfx_fill(gfx_ctx_t *ctx, uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    for (uint32_t i = 0; i < h && (y + i) < ctx->height; i++) {
        for (uint32_t j = 0; j < w && (x + j) < ctx->width; j++) {
            ctx->buffer[(y + i) * ctx->stride + (x + j)] = color;
        }
    }
}

void gfx_rect(gfx_ctx_t *ctx, uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    for (uint32_t j = 0; j < w && (x + j) < ctx->width; j++) {
        gfx_putpixel(ctx, x + j, y, color);
        gfx_putpixel(ctx, x + j, y + h - 1, color);
    }
    for (uint32_t i = 0; i < h && (y + i) < ctx->height; i++) {
        gfx_putpixel(ctx, x, y + i, color);
        gfx_putpixel(ctx, x + w - 1, y + i, color);
    }
}

void gfx_draw_char(gfx_ctx_t *ctx, uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg) {
    if ((unsigned char)c > 127) c = '?';
    const uint8_t *glyph = font8x16[(uint8_t)c];
    for (int i = 0; i < 16; i++) {
        for (int j = 0; j < 8; j++) {
            if (glyph[i] & (1 << (7 - j))) {
                gfx_putpixel(ctx, x + j, y + i, fg);
            } else if (bg != 0xFF000000) { // Using 0xFF000000 as "transparent"
                gfx_putpixel(ctx, x + j, y + i, bg);
            }
        }
    }
}

void gfx_draw_text(gfx_ctx_t *ctx, uint32_t x, uint32_t y, const char *text, uint32_t fg, uint32_t bg) {
    uint32_t cur_x = x;
    while (*text) {
        gfx_draw_char(ctx, cur_x, y, *text++, fg, bg);
        cur_x += 8;
    }
}

void gfx_blit(gfx_ctx_t *dst, gfx_ctx_t *src, uint32_t dx, uint32_t dy) {
    for (uint32_t i = 0; i < src->height && (dy + i) < dst->height; i++) {
        for (uint32_t j = 0; j < src->width && (dx + j) < dst->width; j++) {
            dst->buffer[(dy + i) * dst->stride + (dx + j)] = src->buffer[i * src->stride + j];
        }
    }
}

uint32_t gfx_lerp_color(uint32_t c1, uint32_t c2, uint8_t t) {
    uint32_t r1 = (c1 >> 16) & 0xFF, g1 = (c1 >> 8) & 0xFF, b1 = c1 & 0xFF;
    uint32_t r2 = (c2 >> 16) & 0xFF, g2 = (c2 >> 8) & 0xFF, b2 = c2 & 0xFF;
    uint32_t r = r1 + ((r2 - r1) * t >> 8);
    uint32_t g = g1 + ((g2 - g1) * t >> 8);
    uint32_t b = b1 + ((b2 - b1) * t >> 8);
    return (r << 16) | (g << 8) | b;
}

void gfx_gradient_v(gfx_ctx_t *ctx, uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t c1, uint32_t c2) {
    for (uint32_t i = 0; i < h && (y + i) < ctx->height; i++) {
        uint32_t color = gfx_lerp_color(c1, c2, (i * 255) / h);
        for (uint32_t j = 0; j < w && (x + j) < ctx->width; j++) {
            ctx->buffer[(y + i) * ctx->stride + (x + j)] = color;
        }
    }
}

void gfx_gradient_h(gfx_ctx_t *ctx, uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t c1, uint32_t c2) {
    for (uint32_t j = 0; j < w && (x + j) < ctx->width; j++) {
        uint32_t color = gfx_lerp_color(c1, c2, (j * 255) / w);
        for (uint32_t i = 0; i < h && (y + i) < ctx->height; i++) {
            ctx->buffer[(y + i) * ctx->stride + (x + j)] = color;
        }
    }
}

void gfx_fill_rounded(gfx_ctx_t *ctx, uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color, int r) {
    gfx_fill(ctx, x, y, w, h, color);
    if (r >= 2 && w >= 4 && h >= 4) {
        /* blank 2x2 at each corner */
        gfx_fill(ctx, x, y, 2, 2, 0xFF000000);
        gfx_fill(ctx, x + w - 2, y, 2, 2, 0xFF000000);
        gfx_fill(ctx, x, y + h - 2, 2, 2, 0xFF000000);
        gfx_fill(ctx, x + w - 2, y + h - 2, 2, 2, 0xFF000000);
    }
}
